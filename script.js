// Interactive floating elements
function initFloatingElements() {
    const floatingTags = document.querySelectorAll('.floating-tag');
    const floatingSkills = document.querySelectorAll('.floating-skill');
    
    // Add click effects to HTML tags
    floatingTags.forEach(tag => {
        tag.addEventListener('click', function() {
            this.style.transform = 'scale(1.3) rotate(360deg)';
            this.style.background = 'rgba(255, 255, 255, 0.3)';
            this.style.color = 'rgba(255, 255, 255, 1)';
            
            setTimeout(() => {
                this.style.transform = '';
                this.style.background = '';
                this.style.color = '';
            }, 1000);
        });
    });
    
    // Add click effects to skill icons
    floatingSkills.forEach(skill => {
        skill.addEventListener('click', function() {
            const skillName = this.getAttribute('data-skill');
            this.style.transform = 'scale(1.4) rotate(15deg)';
            this.style.boxShadow = '0 15px 40px rgba(0, 0, 0, 0.3)';
            
            // Show skill name tooltip
            showSkillTooltip(skillName, this);
            
            setTimeout(() => {
                this.style.transform = '';
                this.style.boxShadow = '';
            }, 1500);
        });
    });
}

// Show skill tooltip
function showSkillTooltip(skillName, element) {
    const tooltip = document.createElement('div');
    tooltip.className = 'skill-tooltip';
    tooltip.textContent = skillName.toUpperCase();
    tooltip.style.cssText = `
        position: absolute;
        background: rgba(0, 0, 0, 0.8);
        color: white;
        padding: 8px 12px;
        border-radius: 6px;
        font-size: 12px;
        font-weight: bold;
        z-index: 1000;
        pointer-events: none;
        transform: translateY(-40px);
        opacity: 0;
        transition: opacity 0.3s ease;
    `;
    
    element.appendChild(tooltip);
    
    setTimeout(() => {
        tooltip.style.opacity = '1';
    }, 100);
    
    setTimeout(() => {
        tooltip.style.opacity = '0';
        setTimeout(() => {
            if (tooltip.parentNode) {
                tooltip.remove();
            }
        }, 300);
    }, 1000);
}

